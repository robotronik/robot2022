float cos_precalc(float angle)
{
	int iangle = angle * 40;
	if(angle<0){
		iangle = 14400-(-iangle % 14400);
	}
	else{
		iangle = iangle % 14400;
	}	
	if(iangle>=0 && iangle<3600){
		return t_cos_precalc[iangle];
	}
	else if(iangle>=3600 && iangle<7200){
		return -t_cos_precalc[7200-iangle];
	}
	else if(iangle>=7200 && iangle<10800){
		return -t_cos_precalc[-(7200-iangle)];
	}
	return t_cos_precalc[(14400-iangle)];
}



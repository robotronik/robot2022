float sin_precalc(float angle)
{	
	return cos_precalc(angle-90);
}

float absfloat(float angle)
{	
	if(angle < 0){
		return -angle;
	}
	return angle;
}

double mod_angle(double a){
	a = fmod(a,360);
	if(a>180){
		a -=360;
	}
	else if(a<-180){
		a +=360;
	}
	return a;
}